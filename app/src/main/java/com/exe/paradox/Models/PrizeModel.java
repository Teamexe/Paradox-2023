package com.exe.paradox.Models;

public class PrizeModel {
    String image;
    String url;
    String title;
    String body;

    public PrizeModel() {
    }

    public String getUrl() {
        return url;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }

    public String getImage() {
        return image;
    }
}
